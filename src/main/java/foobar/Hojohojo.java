package foobar;

class Hojohojo {

    private final String hojo;
    private final String asd;

    Hojohojo(String hojo, String asd) {
        this.hojo = hojo;
        this.asd = asd;
    }

    public String getHojo() {
        return hojo;
    }

    public String getAsd() {
        return asd;
    }
}
